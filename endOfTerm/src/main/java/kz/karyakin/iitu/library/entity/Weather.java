package kz.karyakin.iitu.library.entity;

import com.fasterxml.jackson.annotation.JsonBackReference;
import com.fasterxml.jackson.annotation.JsonIgnore;
import io.swagger.annotations.ApiModelProperty;
import kz.karyakin.iitu.library.enums.WeatherStatus;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.NonNull;

import javax.persistence.*;
import java.util.List;

@Entity
@Data
@NoArgsConstructor
public class Weather {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @ApiModelProperty(value = "ID is generated by database")
    private Long id;

    @NonNull
    private String title;

    @NonNull
    private String description;

    @Enumerated(EnumType.STRING)
    @NonNull
    private WeatherStatus weatherStatus = WeatherStatus.BEST;




    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "broadcasters_id")
    @JsonBackReference(value = "broadcasters")
    @NonNull
    private Broadcasters broadcasters;

    @OneToMany(mappedBy = "weather", cascade = CascadeType.ALL)
    @JsonIgnore
    private List<ReleasedWeather> issuedBooks;

}
