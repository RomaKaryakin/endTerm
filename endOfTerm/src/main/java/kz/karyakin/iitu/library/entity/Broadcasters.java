package kz.karyakin.iitu.library.entity;

import com.fasterxml.jackson.annotation.JsonManagedReference;
import io.swagger.annotations.ApiModelProperty;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.NonNull;

import javax.persistence.*;
import java.util.HashSet;
import java.util.Set;

@Entity
@Data
@NoArgsConstructor
public class Broadcasters {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @ApiModelProperty(value = "ID is generated by database")
    private Long id;

    @NonNull
    private String name;

    @OneToMany(mappedBy = "broadcasters", cascade = CascadeType.ALL)
    @JsonManagedReference(value = "broadcasters")
    private Set<Weather> weathers = new HashSet<>();

}
